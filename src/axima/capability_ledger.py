"""AXIMA Capability Ledger — machine-readable feature status tracking.

Each feature has four lifecycle states:
- IMPLEMENTED: Code exists in the codebase
- WIRED: Code is connected to the router/API
- VERIFIED: Automated tests pass for this feature
- VALIDATED: Eval benchmarks confirm real-world performance

The ledger is generated by introspecting the actual filesystem and eval results.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field, asdict
from enum import Enum
from pathlib import Path
from typing import Sequence


class FeatureState(Enum):
    """Lifecycle state of a feature."""

    IMPLEMENTED = "IMPLEMENTED"  # Code exists
    WIRED = "WIRED"              # Connected to router
    VERIFIED = "VERIFIED"        # Tests pass
    VALIDATED = "VALIDATED"      # Evals confirm performance


@dataclass(frozen=True)
class FeatureStatus:
    """Status of a single feature across all lifecycle states."""

    name: str
    category: str
    implemented: bool = False
    wired: bool = False
    verified: bool = False
    validated: bool = False

    @property
    def state(self) -> FeatureState:
        """Return the highest achieved lifecycle state."""
        if self.validated:
            return FeatureState.VALIDATED
        if self.verified:
            return FeatureState.VERIFIED
        if self.wired:
            return FeatureState.WIRED
        if self.implemented:
            return FeatureState.IMPLEMENTED
        return FeatureState.IMPLEMENTED  # minimum — code must exist to be in ledger

    def to_dict(self) -> dict[str, object]:
        """Serialize to dict for JSON output."""
        return {
            "name": self.name,
            "category": self.category,
            "state": self.state.value,
            "implemented": self.implemented,
            "wired": self.wired,
            "verified": self.verified,
            "validated": self.validated,
        }


@dataclass
class CapabilityLedger:
    """Complete capability ledger for the AXIMA system."""

    features: list[FeatureStatus] = field(default_factory=list)

    def add(self, feature: FeatureStatus) -> None:
        """Add a feature to the ledger."""
        self.features.append(feature)

    def to_json(self, indent: int = 2) -> str:
        """Serialize ledger to JSON."""
        data = {
            "version": "0.1.0",
            "total_features": len(self.features),
            "summary": self._summary(),
            "features": [f.to_dict() for f in self.features],
        }
        return json.dumps(data, indent=indent)

    def _summary(self) -> dict[str, int]:
        """Count features by highest achieved state."""
        counts = {s.value: 0 for s in FeatureState}
        for f in self.features:
            counts[f.state.value] += 1
        return counts

    @property
    def categories(self) -> list[str]:
        """Return sorted list of unique categories."""
        return sorted(set(f.category for f in self.features))


def _check_file_exists(base: Path, *candidates: str) -> bool:
    """Check if any of the candidate paths exist."""
    for c in candidates:
        if (base / c).exists():
            return True
    return False


def _check_eval_exists(evals_dir: Path, category: str) -> bool:
    """Check if eval cases exist for a category."""
    cases_file = evals_dir / category / "cases.json"
    return cases_file.is_file()


