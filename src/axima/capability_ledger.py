    print("=" * 70)
    print(f"{'Feature':<30} {'Category':<14} {'State':<12} I  W  V  D")
    print("-" * 70)

    for f in ledger.features:
        i = "✓" if f.implemented else "·"
        w = "✓" if f.wired else "·"
        v = "✓" if f.verified else "·"
        d = "✓" if f.validated else "·"
        print(f"  {f.name:<28} {f.category:<14} {f.state.value:<12} {i}  {w}  {v}  {d}")

    print("-" * 70)
    summary = ledger._summary()
    print(f"  Total: {len(ledger.features)} features")
    for state, count in summary.items():
        if count > 0:
            print(f"    {state}: {count}")
    print()


